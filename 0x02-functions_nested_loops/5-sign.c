#include "main.h"
/**
*print_sign - printing out a positive or negative for a number
*@n: character to be described
*Return: nothing else
*/
int print_sign(int n)
{
	if (n > 0)
	{
		_putchar('+');
		return (1);
	}
	else if (n < 0)
	{
		_putchar('-');
		return (-1);
	}
	else
	{
		_putchar('0');
		return (0);
	}
}
